# Assemble HTML templates and minify resulting files.
sub assemble
{
	my ($src) = @_;
	my $dir = dirname($src);
	$src = basename($src);
	(my $dest = $src) =~ s/.tpl$//;
	my $t = Template->new({
		INCLUDE_PATH	=> "$dir",
		OUTPUT_PATH	=> "$dir",
	});

	say "Assemble template $src -> $dest";

	my ($table, $table_size) = generate_table();
	my $t_vals = {
		TABLE_SIZE => $table_size,
		TABLE => $table
	};
	$t->process($src, $t_vals, $dest)
		|| die $t->error(), "\n";
}


